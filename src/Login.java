    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Attempt to authenticate user
        String email = txtemail.getText();
        String password = txtpassword.getText();

        int temp = 0;
        try {
            Connection con = ConnectionProvider.getcon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from appuser where email='" + email + "' and password='" + password + "'and status='Active'");
            while(rs.next()){
                // Login successful, proceed to home screen
                temp=1;
                setVisible(false);
                new Home(rs.getString("userRole")).setVisible(true);
            }
            if(temp==0){
                 // Login failed, show error message
              
                JOptionPane.showMessageDialog(rootPane, "Incorrect Email or password!",  "Error", 0);
            }
        } catch (Exception e) {
            // Handle any exceptions
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

   
    
